#include<iostream>
using namespace std;
int main(){
    int num1,num2;
    cout<<"Enter the value of num1:\n"; // cout<< is called insertion operator
    cin>>num1; //cin is called extraction operator
    cout<<"Enter the value of num2:\n"; // cout<< is called insertion operator
    cin>>num2; //cin is called extraction operator
    cout<<"sum of both the numbers are "<<num1+num2;
    return 0;
}